package com.tesla.colegio.model.bean;

public class AulaCapacidad {
	Integer capacidad;
	Integer nro_vac;
	Integer id_au_sug;
	String tip_cronograma;

	public Integer getCapacidad() {
		return capacidad;
	}

	public void setCapacidad(Integer capacidad) {
		this.capacidad = capacidad;
	}

	public Integer getNro_vac() {
		return nro_vac;
	}

	public void setNro_vac(Integer nro_vac) {
		this.nro_vac = nro_vac;
	}

	public Integer getId_au_sug() {
		return id_au_sug;
	}

	public void setId_au_sug(Integer id_au_sug) {
		this.id_au_sug = id_au_sug;
	}

	public String getTip_cronograma() {
		return tip_cronograma;
	}

	public void setTip_cronograma(String tip_cronograma) {
		this.tip_cronograma = tip_cronograma;
	}
}
